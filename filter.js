    normalized = applyPhraseEquivalents(normalized);
    // 4. å†è¿›è¡Œå­—ç¬¦çº§ç¹ä½“â†’ç®€ä½“è½¬æ¢ï¼ˆç»Ÿä¸€æ ‡å‡†ï¼‰
    normalized = convertTraditionalToSimplified(normalized);
